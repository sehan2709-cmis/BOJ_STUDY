# 1. 입력 받기
n, m = map(int, input().split())
# x: 서쪽으로부터 거리(가로/열), y: 북쪽으로부터 거리(세로/행)
y, x, look = map(int, input().split())

# 2. 전체 맵 정보 입력 받기
landmap = []
for _ in range(n):
    landmap.append(list(map(int, input().split())))

# 방문 위치를 저장할 맵 (0으로 초기화)
visited = [[0] * m for _ in range(n)]
visited[y][x] = 1 # 현재 좌표 방문 처리

# 3. 방향 설정 (북, 동, 남, 서 순서) - 중요!
# dy는 세로 변화량, dx는 가로 변화량
dy = [-1, 0, 1, 0] 
dx = [0, 1, 0, -1]

# 왼쪽으로 회전하는 함수
def turn_left():
    global look
    look -= 1
    if look == -1:
        look = 3

# 4. 시뮬레이션 시작
count = 1 # 방문한 칸의 수
turn_time = 0 # 회전 횟수 (4번 돌았는지 확인용)

while True:
    # 1단계: 왼쪽으로 회전
    turn_left()
    ny = y + dy[look]
    nx = x + dx[look]
    
    # 2단계: 가보지 않은 육지가 있다면 이동
    if visited[ny][nx] == 0 and landmap[ny][nx] == 0:
        visited[ny][nx] = 1
        y, x = ny, nx
        count += 1
        turn_time = 0 # 이동 후 회전 횟수 초기화
        continue
    # 가보지 않은 육지가 없거나 바다라면 회전만 하고 다시 체크
    else:
        turn_time += 1
        
    # 3단계: 네 방향 모두 갈 수 없는 경우
    if turn_time == 4:
        # 바라보는 방향 유지한 채 뒤로 한 칸 가기 위한 좌표 계산
        ny = y - dy[look]
        nx = x - dx[look]
        
        # 뒤로 갈 수 있다면 이동 (육지인 경우만)
        if landmap[ny][nx] == 0:
            y, x = ny, nx
            turn_time = 0 # 다시 탐색 시작
        # 뒤가 바다라면 멈춤
        else:
            break

print(count)

