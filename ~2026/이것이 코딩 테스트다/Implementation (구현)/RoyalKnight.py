#두 번째 코드
data = input() # 1. 입력 받기 및 초기 위치 설정
x = ord(data[0]) - ord('a') + 1 # 열(Column): a -> 1
y = int(data[1]) # 행(Row): 1 -> 1

moves = [ # 2. 나이트가 이동할 수 있는 8가지 방향 정의 (방향 벡터)
    (2, 1), (2, -1), (-2, 1), (-2, -1),
    (1, 2), (1, -2), (-1, 2), (-1, -2)
]
result = 0

# 3. 이동 가능 여부 확인
for dx, dy in moves:
    nx, ny = x + dx, y + dy
    
    # 8x8 평면 안에 있는지 확인
    if 1 <= nx <= 8 and 1 <= ny <= 8:
        result += 1

print(result)

