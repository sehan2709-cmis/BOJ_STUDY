import sys

N = int(sys.stdin.readline()) #sys.stdin.readline() 활용하기
straws = sorted([int(sys.stdin.readline()) for _ in range(N)], reverse=True) #리스트 컴프리헨션 활용

for i in range(N-2): #while loop 말고 for loop으로 코딩
    if(straws[i] < straws[i+1] + straws[i+2]):
        print(sum(straws[i:i+3])) #sum() 써보기
        break
else: #for ... else 문법 활용
    print(-1)

