#코드:
T = int(input()) # 테스트 케이스 개수
n = [int(input()) for _ in range(T)] #정수 받기

answer = [0, 1, 2, 4] #0은 버리는 용

for i in range(4, 11):
    answer.append(answer[i-1] + answer[i-2] + answer[i-3])

for i in n:
    print(answer[i])

